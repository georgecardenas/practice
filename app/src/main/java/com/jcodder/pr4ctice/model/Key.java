package com.jcodder.pr4ctice.model;

public class Key {
    private int fifths;
    private String mode;
    private String glyphs;

    public int getFifths() {
        return fifths;
    }

    public void setFifths(int fifths) {
        this.fifths = fifths;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public String getGlyphs() {
        return glyphs;
    }

    public void setGlyphs(String glyphs) {
        this.glyphs = glyphs;
    }
}
